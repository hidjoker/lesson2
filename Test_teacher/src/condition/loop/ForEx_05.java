package condition.loop;

public class ForEx_05 {
	public static void main(String[] args) {
		// "Hello"를 7번 출력하시오
	
		//반복횟수 : 7회
		//반복구간 : 0~6
		
		//초기식 : int i=0;
		//조건식 : i<7; 
		//증감식 : i++
		
		for( int i=0; i<7; i++ ) {
			System.out.println("Hello");
		}
		
	}
}














