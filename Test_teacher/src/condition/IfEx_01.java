package condition;

public class IfEx_01 {
	public static void main(String[] args) {
		
		// if문
		// 제어문 if
		// 조건문

		// if(조건식)
		//	조건식이 참일 때 수행하는 코드
	
		if(true) {
			System.out.println("출력이 되려나??");
		}
		
		if(false) {
			System.out.println("블럭내 첫번째 코드");
			System.out.println("출력 될까요??");
		}
		
		System.out.println("여긴 if문 밖");
		
	}
}









