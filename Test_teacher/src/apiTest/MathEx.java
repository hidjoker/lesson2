package apiTest;

public class MathEx {
	public static void main(String[] args) {
		
		System.out.println("PI : " + Math.PI );
		
		System.out.println( Math.abs(-10) );// -10의 절대값
		System.out.println( Math.ceil(3.7) );// 3.7의 올림값
		System.out.println( Math.floor(3.7) );// 3.7의 내림값
		System.out.println( Math.pow(2.1, 3) );// 2.1의 3제곱값
		System.out.println( Math.random() );// 0.0(포함) ~ 1.0(제외) 사이의 난수(랜덤)
		System.out.println( Math.exp(3.3) );// 자연상수 e의 3.3제곱

		System.out.println( Math.ceil(-3.7) );// -3.7의 올림값
		System.out.println( Math.floor(-3.7) );// -3.7의 내림값
		
	}
}







